   private void showAllPatient()
    {
    try
    {
    ArrayList<PatientPojo> emplist=PatientDao.getAllPatients();
    Object rows[]=new Object[12];
    DefaultTableModel dtm=(DefaultTableModel)jtPData.getModel();
    for(PatientPojo e:emplist)
    {
    rows[4]=e.getOpd();
    rows[11]=e.getDoctor_id();
    rows[1]=e.getF_name();
    rows[2]=e.getS_name();
    rows[3]=e.getAge();
    rows[0]=e.getP_id();
    rows[5]=e.getGender();
    rows[6]=e.getM_status();
    rows[8]=e.getAddress();
    rows[9]=e.getCity();
    rows[10]=e.getMno();
     rows[7]=e.getDate();


    dtm.addRow(rows);
    }
    }
    catch(SQLException ex)
    {
    JOptionPane.showMessageDialog(null,"DbError:"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
    }
    }
    
      
