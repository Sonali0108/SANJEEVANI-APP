    private void showEmpData()
    {
    try
    {
      ArrayList<EmpPojo> emplist=EmpDao.getAllEmp();
      Object rows[]=new Object[4];
      DefaultTableModel dtm=(DefaultTableModel)jtEmpData.getModel();
      for(EmpPojo e:emplist)
      {
      rows[0]=e.getEmpid();
      rows[1]=e.getEmpname();
      rows[2]=e.getJob();
      rows[3]=e.getSal();
      dtm.addRow(rows);
      }
    }
        catch(SQLException ex)
                {
                JOptionPane.showMessageDialog(null,"Sql error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();

                }
   
    
    }

